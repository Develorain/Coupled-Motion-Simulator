package com.ahmad.Tools;

public class MathTools {
    public static double sin(double angleDegrees) {
        return java.lang.Math.sin(java.lang.Math.toRadians(angleDegrees));
    }

    public static double cos(double angleDegrees) {
        return java.lang.Math.cos(java.lang.Math.toRadians(angleDegrees));
    }

    public static double tan(double angleDegrees) {
        return java.lang.Math.tan(java.lang.Math.toRadians(angleDegrees));
    }

    public static double asin(double angleDegrees) {
        return java.lang.Math.asin(java.lang.Math.toRadians(angleDegrees));
    }

    public static double acos(double angleDegrees) {
        return java.lang.Math.acos(java.lang.Math.toRadians(angleDegrees));
    }

    public static double atan(double angleDegrees) {
        return java.lang.Math.atan(java.lang.Math.toRadians(angleDegrees));
    }
}
